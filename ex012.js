// Switch-Case



let fruta = "banana"

switch (fruta){
    case "laranja":
        console.log("Suco de laranja")
        break
    case "banana":
        console.log("Vitamina de banana")
        break
    case "maçã":
        console.log("suco de maçã")
        break
    default:
        console.log("suco genérico")
}